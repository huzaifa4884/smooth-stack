package com.smoothstack.jdass;

public class Student {
    int id;
    String name;
    int SchoolId;

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSchoolId(int schoolId) {
        SchoolId = schoolId;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getSchoolId() {
        return SchoolId;
    }
}
